-- 6. RLS + POLICIES
-- =========================
-- Since this app uses manual app-level login instead of Supabase Auth,
-- these policies allow the GitHub Pages frontend to read/write with the anon key.
-- Keep your GitHub repository private if you do not want your Supabase project visible.

alter table public.app_users enable row level security;
alter table public.role_permissions enable row level security;
alter table public.menu_items enable row level security;
alter table public.sales enable row level security;
alter table public.expenses enable row level security;
alter table public.inventory enable row level security;
alter table public.sop_recipes enable row level security;
alter table public.sop_ingredients enable row level security;
alter table public.recipe_preparations enable row level security;
alter table public.inventory_transactions enable row level security;

do $$
declare
  t text;
begin
  foreach t in array array[
    'app_users',
    'role_permissions',
    'menu_items',
    'sales',
    'expenses',
    'inventory',
    'sop_recipes',
    'sop_ingredients',
    'recipe_preparations',
    'inventory_transactions'
  ]
  loop
    if not exists (
      select 1 from pg_policies
      where schemaname = 'public'
      and tablename = t
      and policyname = 'app anon select'
    ) then
      execute format('create policy "app anon select" on public.%I for select to anon using (true)', t);
    end if;

    if not exists (
      select 1 from pg_policies
      where schemaname = 'public'
      and tablename = t
      and policyname = 'app anon insert'
    ) then
      execute format('create policy "app anon insert" on public.%I for insert to anon with check (true)', t);
    end if;

    if not exists (
      select 1 from pg_policies
      where schemaname = 'public'
      and tablename = t
      and policyname = 'app anon update'
    ) then
      execute format('create policy "app anon update" on public.%I for update to anon using (true) with check (true)', t);
    end if;

    if not exists (
      select 1 from pg_policies
      where schemaname = 'public'
      and tablename = t
      and policyname = 'app anon delete'
    ) then
      execute format('create policy "app anon delete" on public.%I for delete to anon using (true)', t);
    end if;
  end loop;
end $$;
