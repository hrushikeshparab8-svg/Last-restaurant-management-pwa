create or replace function public.update_inventory_from_expense()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  existing_inventory_id bigint;
  current_added numeric := 0;
  current_avg numeric := 0;
  current_qty numeric := 0;
  new_rate numeric := 0;
  calculated_avg numeric := 0;
begin
  if new.quantity is null or new.quantity <= 0 then
    return new;
  end if;

  new_rate := coalesce(new.total_price, new.buying_price, 0) / new.quantity;

  select id, stock_added, average_purchase_price
  into existing_inventory_id, current_added, current_avg
  from public.inventory
  where lower(raw_material_name) = lower(new.item_name)
  order by id desc
  limit 1;

  if existing_inventory_id is not null then
    current_qty := coalesce(current_added, 0);

    calculated_avg := case
      when (current_qty + new.quantity) > 0
      then ((current_qty * coalesce(current_avg, 0)) + (new.quantity * new_rate)) / (current_qty + new.quantity)
      else new_rate
    end;

    update public.inventory
    set stock_added = coalesce(stock_added, 0) + new.quantity,
        category = coalesce(new.category, category),
        unit = coalesce(new.unit, unit),
        last_purchase_price = new_rate,
        average_purchase_price = calculated_avg,
        purchase_price = coalesce(new.total_price, new.buying_price, purchase_price),
        updated_at = now()
    where id = existing_inventory_id;
  else
    insert into public.inventory (
      raw_material_name,
      category,
      unit,
      opening_stock,
      stock_added,
      stock_used,
      minimum_stock_level,
      purchase_price,
      last_purchase_price,
      average_purchase_price
    )
    values (
      new.item_name,
      coalesce(new.category, 'Raw Material'),
      coalesce(new.unit, 'pieces'),
      0,
      new.quantity,
      0,
      0,
      coalesce(new.total_price, new.buying_price, 0),
      new_rate,
      new_rate
    );
  end if;

  insert into public.inventory_transactions (
    item_name, category, unit, transaction_type, quantity, amount,
    reference_table, reference_id, transaction_date
  )
  values (
    new.item_name, coalesce(new.category, 'Raw Material'), new.unit, 'purchase',
    new.quantity, coalesce(new.total_price, new.buying_price, 0),
    'expenses', new.id, new.expense_date
  );

  new.inventory_updated := true;
  return new;
end;
$$;

drop trigger if exists trg_update_inventory_from_expense on public.expenses;

create trigger trg_update_inventory_from_expense
before insert on public.expenses
for each row
execute function public.update_inventory_from_expense();

