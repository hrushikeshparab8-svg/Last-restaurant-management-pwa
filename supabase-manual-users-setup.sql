create or replace function public.app_login(p_username text, p_password text)
returns table (
  id bigint,
  username text,
  display_name text,
  role text,
  active boolean
)
language sql
security definer
set search_path = public
as $$
  select u.id, u.username, u.display_name, u.role, u.active
  from public.app_users u
  where lower(u.username) = lower(p_username)
    and u.password_hash = crypt(p_password, u.password_hash)
    and u.active = true
  limit 1;
$$;

create or replace function public.app_create_user(
  p_username text,
  p_password text,
  p_display_name text,
  p_role text
)
returns public.app_users
language plpgsql
security definer
set search_path = public
as $$
declare
  new_user public.app_users;
begin
  if p_role not in ('admin','manager','staff','inventory_user') then
    raise exception 'Invalid role';
  end if;

  insert into public.app_users (username, display_name, password_hash, role, active)
  values (lower(trim(p_username)), p_display_name, crypt(p_password, gen_salt('bf')), p_role, true)
  returning * into new_user;

  return new_user;
end;
$$;

create or replace function public.app_update_user(
  p_id bigint,
  p_display_name text,
  p_role text,
  p_active boolean
)
